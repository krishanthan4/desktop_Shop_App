    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        if (email.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please enter your email", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            
            JOptionPane.showMessageDialog(this, "Invalid Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your password", "Warning", JOptionPane.WARNING_MESSAGE);

        }  else {
            System.out.println("ok");
        }

        
        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `employee` WHERE `email`='"+email+"' AND `password`='"+password+"'");
            
            if(resultset.next()){
                
                String fname = resultset.getString("first_name");
                                String lname = resultset.getString("last_name");

                                
                Home home = new Home(email,fname,lname);
                home.setVisible(true);
this.dispose();

            }else{
            
            JOptionPane.showMessageDialog(this, "Invalid deatails","Warning",JOptionPane.WARNING_MESSAGE);
            
            }
            
        } catch (Exception e) {
        e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

